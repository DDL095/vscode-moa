/**
 * 在 `.moa_cache/` 目录下确保有一份 README.md。
 *
 * 调用时机：**任意写入 `.moa_cache/` 的代码路径**在创建目录时调用一次。
 * 已存在则不覆盖（尊重用户修改）。
 *
 * @param cacheRoot 例如 `<workspace>/.moa_cache`
 * @returns 写入成功返回 true；已存在 / 失败 / workspace 不可用 返回 false
 */
export function ensureCacheReadme(cacheRoot: string): boolean {
  try {
    if (!cacheRoot || typeof cacheRoot !== 'string') return false;

    // 目录不存在先尝试创建（理论上调用方已创建，但稳妥起见）
    if (!fs.existsSync(cacheRoot)) {
      fs.mkdirSync(cacheRoot, { recursive: true });
    }

    const readmePath = path.join(cacheRoot, 'README.md');

    // v0.21.3: 版本升级时强制覆盖（让用户看到新的 autopilot 警告）
    //   读现有 README 头部的 "Cache README vN" 提取版本号
    //   若不存在或版本低于 CACHE_README_VERSION → 覆盖
    //   若版本等于当前 → 不覆盖（尊重用户修改）
    if (fs.existsSync(readmePath)) {
      try {
        const existing = fs.readFileSync(readmePath, 'utf8');
        const m = existing.match(/Cache README v(\d+)/);
        const existingVer = m ? parseInt(m[1], 10) : 0;
        if (existingVer >= CACHE_README_VERSION) {
          return false;  // 已是最新版本，不覆盖
        }
        // 版本低于当前 → 继续覆盖
      } catch {
        // 读失败 → 不覆盖（保守）
        return false;
      }
    }

    const content = buildCacheReadmeContent();

    // 原子写入：先写 tmp 再 rename，避免被并发读取到半写状态
    const tmpPath = `${readmePath}.tmp`;
    fs.writeFileSync(tmpPath, content, 'utf8');
    fs.renameSync(tmpPath, readmePath);

    return true;
  } catch (err) {
    // 写失败不阻塞主流程，只 console.warn
    const msg = err instanceof Error ? err.message : String(err);
    console.warn(`[MoA cacheReadme] failed to write README.md: ${msg}`);
    return false;
  }
}

/**
 * 便捷封装：直接从 vscode.workspace 推断 cacheRoot 并写入。
 *
 * 用于不想自己拼路径的调用方（典型场景）。
 */
export function ensureDefaultCacheReadme(): boolean {
  const ws = vscode.workspace.workspaceFolders?.[0]?.uri.fsPath;
  if (!ws) return false;
  const cacheRoot = path.join(ws, '.moa_cache');
  return ensureCacheReadme(cacheRoot);
}
