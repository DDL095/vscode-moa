/**
 * `.moa_cache/` 目录的 README 自动写入器（v0.14.10）
 *
 * 设计动机
 * --------
 * 作为一个会在用户工作区写入文件的插件，应该让用户随时能知道：
 *   1. 这个 `.moa_cache/` 目录是什么、谁写的、干什么用的
 *   2. 每个子目录的用途和保留价值（哪些是缓存可删、哪些是审计留档）
 *   3. 出问题时怎么诊断（识别 1213 dump、recon trace 等）
 *   4. 不需要时怎么清理（整体删除、按子目录删除、加 .gitignore）
 *   5. 想从缓存里挖有用内容（如 ref 输出、aggregator 综合）怎么定位
 *
 * 解法
 * ----
 * 任意一个写入 `.moa_cache/` 的代码路径（l3Summarizer / moaOrchestrator /
 * moaRunner.reconDump）在**首次创建目录时**调用 `ensureCacheReadme()`，
 * 在 `.moa_cache/README.md` 原子写入一份说明文档。
 *
 * - 已存在 README.md → 不覆盖（用户可能改过）
 * - workspace 不可用 → 静默跳过（不阻塞主流程）
 * - 写失败 → 只 console.warn，不影响 MoA 任务
 *
 * 文档语言：中英双语（中文为主，关键名词附英文），符合开源插件的国际友好度。
 */

