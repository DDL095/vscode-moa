/**
 * Type definitions for MoA Bridge chat participant.
 */

import type { ChatParticipantToolToken } from 'vscode';

/**
 * Execution path for MoA workflow.
 *
 * v0.7.2: only P1 native (vscode.lm) is implemented. The P2a/P2b paths were
 * removed (unimplemented / unused). Kept the union for forward-compat with
 * future direct-HTTP mode.
 */
export type MoaPath = 'P1' | 'P1-partial' | 'P1-degraded' | 'error';

/**
 * Result of running the MoA fan-out + aggregate flow.
 */
export interface MoaRunResult {
  /** Final aggregated output (Markdown). */
  output: string;
  /** Wall-clock elapsed seconds. */
  elapsed: number;
  /** Path that was actually used. */
  path: MoaPath;
}

/**
 * Lightweight structured prompt for vscode.lm.
 */
export interface ChatMessage {
  role: 'system' | 'user' | 'assistant';
  content: string;
}

/**
 * A reference advisor entry — equal-mode MoA (v2).
 *
 * Stored in `moa.refModels` setting (array). All refs share the same prompt
 * (`moa.sharedRefPrompt`); the model difference provides diversity.
 */
export interface RefModelConfig {
  /**
   * Display label for this advisor, e.g. "advisor_1". Auto-generated by
   * the Configure Models command. Not injected into the prompt in v2.
   */
  role: string;
  /** Substring to match against vscode.lm model name, e.g. "GLM-5.2". */
  model: string;
  /**
   * Deprecated in v2 (equal mode). Kept for backward compat — parsed but
   * ignored at runtime. Use `moa.sharedRefPrompt` instead.
   * @deprecated v2 equal-mode MoA ignores per-ref systemHint.
   */
  systemHint?: string;
}

/**
 * Aggregator model config.
 */
export interface AggregatorConfig {
  /** Substring to match against vscode.lm model name. */
  model: string;
  /** Optional temperature (currently informational; vscode.lm doesn't expose per-call temp). */
  temperature?: number;
}

/**
 * v0.14.0: Recon agent 专用模型配置。
 *
 * 设计：
 *   - 用户可通过 "Moa: Configure Models" Step 3/4 单独指定 recon 用的模型
 *   - 留空（model=''）= fallback 到 aggregator 模型（保持 v0.13.x 行为）
 *   - 推荐选工具调用稳定的模型（GLM-5.2 / DeepSeek-V4-Pro）
 *
 * 与 AggregatorConfig 字段一致（model + temperature），但 temperature 实际
 * 未被 vscode.lm API 使用，仅保留以便未来扩展。
 */
export interface ReconConfig {
  /**
   * 模型 ID（vscode.lm 的 m.id，如 "gcmp.zhipu:::GLM-5.2-CodingPlan"）。
   * 空字符串 = fallback 到 aggregator 模型（向后兼容 v0.13.x）。
   */
  model: string;
  /** Optional temperature（暂未使用，预留扩展）。 */
  temperature?: number;
}

// ─────────────────────────────────────────────────────────────────────────
// v0.15.0: 5 显式角色架构
//
// 设计哲学：把原来隐性流水线（recon → refs → aggregator → acting）
// 显式化为 5 个有名字、可独立配置的角色：
//
//   1. Planner  — 任务理解、子问题拆解、首轮规划（强推理）
//   2. Recon    — 调工具收集证据（量大管饱模型）
//   3. Refs     — 多视角并行分析（多模型多样性）
//   4. Aggregator — 融合 + 决策（gate，强推理）
//   5. Actor    — 执行 action_items（全工具权限）
//
// 配置层：PlannerConfig 和 ActorConfig 是 v0.15.0 新增。
// Refs/Aggregator/Recon 沿用现有配置（RefModelConfig/AggregatorConfig/ReconConfig）。
// ─────────────────────────────────────────────────────────────────────────

/**
 * v0.15.0: Planner 角色配置（任务理解 + 规划）。
 *
 * 设计：
 *   - 仅在 orchestrate 首轮调用一次（state.iteration === 0）
 *   - model='' 表示 fallback 到 aggregator 模型
 *   - 推荐用强推理模型（用户主会话 LLM 或 aggregator）
 *
 * Planner 的产出（clarified_task + sub_questions + recon_hints）
 * 注入首轮 Recon 的初始方向。
 */
export interface PlannerConfig {
  /**
   * 模型 ID（vscode.lm 的 m.id）。
   * 空字符串 = fallback 到 aggregator 模型。
   */
  model: string;
  /** Optional temperature（暂未使用）。 */
  temperature?: number;
}

/**
 * v0.15.0: Actor 角色配置（执行 action_items）。
 *
 * 设计：
 *   - Aggregator 标记 `next_action="actor_needed"` 时触发
 *   - model='' 表示 fallback 到 aggregator 模型（沿用 v0.14.x acting 行为）
 *   - 工具权限：全开（readFile/writeFile/run_in_terminal/fetch_webpage/grep/...）
 *
 * Actor 失败时不抛错，把失败原因写入 state.actor_history，
 * 由下一轮 Recon 读取并调查根因。
 */
export interface ActorConfig {
  /**
   * 模型 ID（vscode.lm 的 m.id）。
   * 空字符串 = fallback 到 aggregator 模型。
   */
  model: string;
  /** Optional temperature（暂未使用）。 */
  temperature?: number;
}

/**
 * v0.14.0: L3 孙代理（大文件精选器）模型配置。
 *
 * 设计：
 *   - 用户可通过 "Moa: Configure Models" Step 4/4 单独指定 L3 用的模型
 *   - 留空（model=''）= 完全禁用 L3，所有大文件走 L2 语义边界截断
 *   - 默认推荐 MiniMax-M3（CLAUDE.md §2.4 两层嵌套规则要求 M3）
 *
 * 移植性：把 modelId 从硬编码常量（v0.13.0 的 DEFAULT_L3_MODEL_ID）
 * 改为配置项，避免换环境后失效。
 */
export interface L3Config {
  /**
   * 模型 ID（vscode.lm 的 m.id）。
   * 空字符串 = 禁用 L3（所有大文件降级到 L2 语义边界截断）。
   */
  model: string;
}

// ─────────────────────────────────────────────────────────────────────────
// v0.14.14: Preset group — full-pipeline config bundle.
//
// 设计哲学：
//   - 一个 preset 是整套流水线配置（refs + aggregator + recon + L3）
//   - 通过 `moa.presets` (object) 存储，key 是 preset 名（如 "default", "research"）
//   - `moa.activePreset` 指向当前激活的 key
//   - 运行时统一通过 getActivePresetConfig() 读取（见 presetConfig.ts）
//
// 向后兼容：
//   - 老配置（moa.refModels + moa.aggregator + moa.reconModel + moa.l3Summarizer）
//     在首次访问时自动迁移为 presets.default
//   - 迁移后老字段保留（不删除），作为 fallback
//
// 典型用例：
//   - "code" preset: 4 refs + GLM aggregator + DeepSeek recon + MiniMax L3
//   - "research" preset: 6 refs + MiniMax aggregator + GLM recon + L3 disabled
//   - "quick" preset: 2 refs + GLM aggregator (no recon, no L3)
// ─────────────────────────────────────────────────────────────────────────

/**
 * v0.14.14: 完整流水线预设组。
 *
 * 把原来分散的 4 个配置项（refModels / aggregator / reconModel / l3Summarizer）
 * 打包成一个命名的组，让用户保存多套场景化配置。
 */
export interface MoaPreset {
  /**
   * 显示名（应等于 presets map 的 key）。
   * 仅用于 UI 显示和日志，不参与运行时逻辑。
   */
  name: string;

  /**
   * 参考顾问模型列表（equal-mode MoA）。
   * 沿用现有 RefModelConfig 结构（{role, model}）。
   * 数组长度 = ref 数量（2-8 个）。
   */
  refModels: RefModelConfig[];

  /**
   * 聚合器模型配置。
   */
  aggregator: AggregatorConfig;

  /**
   * Recon 代理模型配置。
   * model='' 表示 fallback 到 aggregator（向后兼容 v0.13.x）。
   *
   * v0.18.0: 此字段仍支持单模型配置（向后兼容），但新增的 `reconModels`
   *（数组）优先级更高。读取顺序：reconModels（非空数组）→ reconModel
   *（单数，自动包装成单元素数组）→ fallback [aggregator]。
   */
  reconModel: ReconConfig;

  /**
   * v0.18.0: 并行 Recon 模型列表。
   *
   * 设计：
   *   - 数组，每个元素是一个 ReconConfig（独立模型 + 可选 temperature）
   *   - 空数组或 undefined = fallback 到 reconModel（单数）→ [aggregator]
   *   - 长度 = 1 = 单模型（自动关闭并行 + 关闭 Recon Aggregator）
   *   - 长度 >= 2 = 并行（受 moa.parallelRecon 开关控制）
   *
   * 并行时每个 Recon 用相同 prompt（含 Planner hints + gaps），
   * 但不同模型对工具的偏好不同（DeepSeek 喜欢多角度搜索，
   * GLM 喜欢链式深入），产出互补。
   */
  reconModels?: ReconConfig[];

  /**
   * v0.18.0: Recon Aggregator（整合多份 Recon 结果）。
   *
   * 设计：
   *   - 仅当 reconModels.length >= 2 且 moa.parallelRecon=true 时启用
   *   - **agent 化整合**（有 full 工具权限，复用 runActingAgent）：
   *     工具用途严格限制在"整合内容所需的辅助操作"
   *     ✅ 允许：读 recon 引用的文件确认细节、读 recon 提到的 URL 复核、
   *              必要时写合并产物到 .moa_cache/
   *     ❌ 禁止：重新做 reconnaissance（新 web search / 学术搜索 / 探索性 grep）
   *     ❌ 禁止：修改用户的源文件
   *   - 核心产出：单一 merged summary（完整无偏差，注入下游 evidence 池）
   *   - model='' = fallback 到 aggregator
   *
   * 设计哲学：整合 ≠ 侦察。整合是"把 N 份完整证据合并成一份无偏差的 summary"，
   * 工具只是辅助手段（如确认 N 个 recon 引用同一文件时的细节），
   * 不应该夹杂新侦察（新侦察是下一轮 Recon 的事，由 Aggregator 标记 gaps 触发）。
   */
  reconAggregator?: ReconConfig;

  /**
   * v0.15.0: Planner 角色配置（任务理解 + 规划）。
   * model='' 表示 fallback 到 aggregator。
   * 未配置（undefined）时：orchestrate 不调用 Planner，走旧逻辑。
   */
  planner?: PlannerConfig;

  /**
   * v0.15.0: Actor 角色配置（执行 action_items）。
   * model='' 表示 fallback 到 aggregator（沿用 v0.14.x acting 行为）。
   * 未配置（undefined）时：orchestrate 的 Actor fallback 到 aggregator。
   */
  actor?: ActorConfig;

  /**
   * L3 孙代理配置。
   * model='' 表示禁用 L3。
   */
  l3Summarizer: L3Config;

  /**
   * 可选：用户自定义描述（"代码项目专用"、"研究任务专用" 等）。
   * 纯 UI 用途，不参与运行时。
   */
  description?: string;

  /**
   * 可选：创建时间戳（ISO 格式），用于排序和审计。
   * 自动生成，用户通常不手编。
   */
  createdAt?: string;
}

